-- This is the table schema for storing heart disease data in PostgreSQL

CREATE TABLE heart_disease_data (
    age INT,
    gender INT,
    cp INT,
    trestbps INT,
    chol INT,
    fbs INT,
    restecg INT,
    thalachh INT,
    exang INT,
    oldpeak FLOAT,
    slope INT,
    ca INT,
    thal INT,
    risk INT
);